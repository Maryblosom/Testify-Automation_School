package com.Mary;
public class Task9a {
    public static void main(String[] args) {
        for (int num = 1; num <= 10; num++) {

            if (num % 2 == 0) {
                System.out.println(num + " is an even number");
            } else if (num % 2 != 0) {
                System.out.println(num + " is an odd number");
            }else{
                System.out.println("Null");
            }
        }
    }
}





























